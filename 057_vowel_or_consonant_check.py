print("Please enter a character: ")
MY_CHAR = input()[0]

if MY_CHAR == 'A' or MY_CHAR == 'a' or MY_CHAR == 'E' or MY_CHAR == 'e' or MY_CHAR == 'I' or MY_CHAR == 'i' or MY_CHAR == 'O' or MY_CHAR == 'o' or MY_CHAR == 'U' or MY_CHAR == 'u': {
    print(f"Character: {MY_CHAR} is a vowel.")
}
else :
    print(f"Character: {MY_CHAR} is a consonant.")